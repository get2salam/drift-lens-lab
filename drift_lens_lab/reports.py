def to_html(result: ExperimentResult) -> str:
    acc_vals = [s[1] for s in result.snapshots]
    loss_vals = [s[2] for s in result.snapshots]
    steps_vals = [s[0] for s in result.snapshots]

    acc_svg = _svg_line(acc_vals, color="#4f8ef7")
    loss_svg = _svg_line(loss_vals, color="#e74c3c")

    drift_rows = ""
    for step, kind in result.drift_events:
        cls = "event-drift" if kind == "drift" else "event-warning"
        drift_rows += f"<tr><td>{step}</td><td class='{cls}'>{html.escape(kind)}</td></tr>\n"
    if not drift_rows:
        drift_rows = "<tr><td colspan='2'><em>No drift events detected.</em></td></tr>"

    acc_str = f"{result.final_accuracy:.4f}" if result.final_accuracy is not None else "n/a"
    loss_str = f"{result.final_log_loss:.4f}" if result.final_log_loss is not None else "n/a"
    drift_at_str = str(result.drift_at) if result.drift_at is not None else "—"

    return f"""<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="utf-8">
<title>DriftLens Lab Report</title>
<style>{_CSS}</style>
</head>
<body>
<h1>DriftLens Lab — Experiment Report</h1>
<table>
  <tr><th>Parameter</th><th>Value</th></tr>
  <tr><td>Steps</td><td>{result.steps}</td></tr>
  <tr><td>Drift kind</td><td><code>{html.escape(result.drift_kind)}</code></td></tr>
  <tr><td>Drift at</td><td>{drift_at_str}</td></tr>
  <tr><td>Seed</td><td>{result.seed}</td></tr>
  <tr><td>Model</td><td>{html.escape(result.model_name)}</td></tr>
  <tr><td>Detector</td><td>{html.escape(result.detector_name)}</td></tr>
</table>

<h2>Metrics</h2>
<div class="metric-card"><div class="val">{acc_str}</div><div class="lbl">Rolling Accuracy</div></div>
<div class="metric-card"><div class="val">{loss_str}</div><div class="lbl">Rolling Log-Loss</div></div>

<h2>Accuracy over time</h2>
{acc_svg}

<h2>Log-Loss over time</h2>
{loss_svg}

<h2>Drift Events</h2>
<table>
  <tr><th>Step</th><th>Kind</th></tr>
  {drift_rows}
</table>

<footer>Generated by <strong>DriftLens Lab</strong></footer>
</body>
</html>
"""


def write_report(result: ExperimentResult, path: str) -> None:
    """Write HTML or Markdown report depending on file extension."""
    import os
    os.makedirs(os.path.dirname(os.path.abspath(path)), exist_ok=True)
    if path.endswith(".md"):
        content = to_markdown(result)
    else:
        content = to_html(result)
    with open(path, "w", encoding="utf-8") as f:
        f.write(content)
