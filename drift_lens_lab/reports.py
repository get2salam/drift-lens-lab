# --------------------------------------------------------------------------- #
# Sparkline helpers (pure text/SVG, no dependencies)                          #
# --------------------------------------------------------------------------- #

def _text_sparkline(values: List[float], width: int = 40) -> str:
    """Return a Unicode block-bar sparkline string."""
    blocks = " ▁▂▃▄▅▆▇█"
    if not values:
        return ""
    lo, hi = min(values), max(values)
    span = hi - lo or 1e-9
    chars = []
    step = max(1, len(values) // width)
    for i in range(0, len(values), step):
        v = values[i]
        idx = int(((v - lo) / span) * (len(blocks) - 1))
        chars.append(blocks[idx])
    return "".join(chars)


def _svg_line(values: List[float], w: int = 400, h: int = 60, color: str = "#4f8ef7") -> str:
    """Return an inline SVG polyline."""
    if len(values) < 2:
        return ""
    lo, hi = min(values), max(values)
    span = hi - lo or 1e-9
    n = len(values)
    pts = []
    for i, v in enumerate(values):
        x = i / (n - 1) * w
        y = h - ((v - lo) / span) * (h - 4) - 2
        pts.append(f"{x:.1f},{y:.1f}")
    points = " ".join(pts)
    return (
        f'<svg width="{w}" height="{h}" viewBox="0 0 {w} {h}" '
        f'xmlns="http://www.w3.org/2000/svg">'
        f'<polyline points="{points}" fill="none" stroke="{color}" stroke-width="2"/>'
        f"</svg>"
    )


# --------------------------------------------------------------------------- #
# Report dataclass                                                             #
# --------------------------------------------------------------------------- #

class ExperimentResult:
    def __init__(
        self,
        *,
        steps: int,
        drift_kind: str,
        drift_at: Optional[int],
        seed: int,
        model_name: str,
        detector_name: str,
        drift_events: List[Tuple[int, str]],         # (step, kind)
        snapshots: List[Tuple[int, float, float]],   # (step, acc, loss)
        final_accuracy: Optional[float],
        final_log_loss: Optional[float],
    ) -> None:
        self.steps = steps
        self.drift_kind = drift_kind
        self.drift_at = drift_at
        self.seed = seed
        self.model_name = model_name
        self.detector_name = detector_name
        self.drift_events = drift_events
        self.snapshots = snapshots
        self.final_accuracy = final_accuracy
        self.final_log_loss = final_log_loss


# --------------------------------------------------------------------------- #
# Markdown                                                                     #
# --------------------------------------------------------------------------- #

def to_markdown(result: ExperimentResult) -> str:
    acc_vals = [s[1] for s in result.snapshots]
    loss_vals = [s[2] for s in result.snapshots]

    lines = [
        "# DriftLens Lab — Experiment Report",
        "",
        f"| Parameter | Value |",
        f"|-----------|-------|",
        f"| Steps | {result.steps} |",
        f"| Drift kind | `{result.drift_kind}` |",
        f"| Drift at | {result.drift_at if result.drift_at is not None else '—'} |",
        f"| Seed | {result.seed} |",
        f"| Model | {result.model_name} |",
        f"| Detector | {result.detector_name} |",
        "",
        "## Metrics",
        "",
        f"- **Final rolling accuracy**: {result.final_accuracy:.4f}" if result.final_accuracy is not None else "- Final rolling accuracy: n/a",
        f"- **Final rolling log-loss**: {result.final_log_loss:.4f}" if result.final_log_loss is not None else "- Final rolling log-loss: n/a",
        "",
        "### Accuracy over time",
        "",
        f"```\n{_text_sparkline(acc_vals)}\n```",
        "",
        "### Log-loss over time",
        "",
        f"```\n{_text_sparkline(loss_vals)}\n```",
        "",
        "## Drift Events",
        "",
    ]

    if result.drift_events:
        lines.append("| Step | Kind |")
        lines.append("|------|------|")
        for step, kind in result.drift_events:
            lines.append(f"| {step} | `{kind}` |")
    else:
        lines.append("_No drift events detected._")

    lines += ["", "---", "_Generated by DriftLens Lab_"]
    return "\n".join(lines)


